package Entity;

/**
 *
 * @author Viet
 */
public class Order {

    private String sid;
    private String sname;
    private double price;
    private int amount;

    public Order() {
    }

    public Order(String sid, String sname, double price, int amount) {
        this.sid = sid;
        this.sname = sname;
        this.price = price;
        this.amount = amount;
    }

    public String getSid() {
        return sid;
    }

    public void setSid(String sid) {
        this.sid = sid;
    }

    public String getSname() {
        return sname;
    }

    public void setSname(String sname) {
        this.sname = sname;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getAmount() {
        return amount;
    }

    public void setAmount(int amount) {
        this.amount = amount;
    }

    

}
